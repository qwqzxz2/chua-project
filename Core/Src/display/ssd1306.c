/**
 * @file    ssd1306.c
 * @brief   SSD1306 OLED 驱动实现 (I2C, 128×64)
 */
#include "display/ssd1306.h"
#include <stdlib.h>

/* 显存缓冲区: 128 * 64 / 8 = 1024 bytes */
static uint8_t SSD1306_Buffer[SSD1306_WIDTH * SSD1306_PAGES];
static I2C_HandleTypeDef *ssd1306_i2c = NULL;
static uint8_t ssd1306_initialized = 0;

/*=================== I2C 通信 ===================*/

static void ssd1306_WriteCmd(uint8_t cmd)
{
    uint8_t data[2] = {0x00, cmd};  /* Co=0, D/C#=0 (command) */
    HAL_I2C_Master_Transmit(ssd1306_i2c, SSD1306_I2C_ADDR, data, 2, 100);
}

static void ssd1306_WriteData(uint8_t *buf, uint16_t len)
{
    /* 数据模式: Co=0, D/C#=1 */
    uint8_t prefix = 0x40;
    HAL_I2C_Master_Transmit(ssd1306_i2c, SSD1306_I2C_ADDR, &prefix, 1, 10);
    for (uint16_t i = 0; i < len; i += 16) {
        uint16_t chunk = (len - i > 16) ? 16 : (len - i);
        HAL_I2C_Master_Transmit(ssd1306_i2c, SSD1306_I2C_ADDR, buf + i, chunk, 100);
    }
}

/*=================== 初始化序列 ===================*/

int SSD1306_Init(I2C_HandleTypeDef *i2c)
{
    ssd1306_i2c = i2c;

    HAL_Delay(10);

    /* 初始化序列 (来自 SSD1306 datasheet) */
    ssd1306_WriteCmd(0xAE);  /* Display OFF */
    ssd1306_WriteCmd(0xD5);  /* Set Display Clock Divide Ratio */
    ssd1306_WriteCmd(0x80);
    ssd1306_WriteCmd(0xA8);  /* Set Multiplex Ratio */
    ssd1306_WriteCmd(0x3F);  /* 64 MUX */
    ssd1306_WriteCmd(0xD3);  /* Set Display Offset */
    ssd1306_WriteCmd(0x00);
    ssd1306_WriteCmd(0x40);  /* Set Display Start Line */
    ssd1306_WriteCmd(0x8D);  /* Charge Pump Setting */
    ssd1306_WriteCmd(0x14);  /* Enable */
    ssd1306_WriteCmd(0x20);  /* Memory Addressing Mode */
    ssd1306_WriteCmd(0x00);  /* Horizontal */
    ssd1306_WriteCmd(0xA1);  /* Segment Re-map (column 127 mapped to SEG0) */
    ssd1306_WriteCmd(0xC8);  /* COM Output Scan Direction (remapped) */
    ssd1306_WriteCmd(0xDA);  /* COM Pins Hardware Configuration */
    ssd1306_WriteCmd(0x12);  /* Alternative pin config */
    ssd1306_WriteCmd(0x81);  /* Set Contrast */
    ssd1306_WriteCmd(0xCF);
    ssd1306_WriteCmd(0xD9);  /* Set Pre-charge Period */
    ssd1306_WriteCmd(0xF1);
    ssd1306_WriteCmd(0xDB);  /* Set VCOMH Deselect Level */
    ssd1306_WriteCmd(0x40);
    ssd1306_WriteCmd(0xA4);  /* Entire Display ON (resume) */
    ssd1306_WriteCmd(0xA6);  /* Normal Display (not inverted) */
    ssd1306_WriteCmd(0x2E);  /* Deactivate Scroll */
    ssd1306_WriteCmd(0xAF);  /* Display ON */

    ssd1306_initialized = 1;
    SSD1306_Clear();
    SSD1306_UpdateScreen();

    return SSD1306_OK;
}

/*=================== 显存操作 ===================*/

void SSD1306_Clear(void)
{
    memset(SSD1306_Buffer, 0, sizeof(SSD1306_Buffer));
}

void SSD1306_DrawPixel(uint8_t x, uint8_t y, uint8_t color)
{
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return;

    if (color) {
        SSD1306_Buffer[x + (y / 8) * SSD1306_WIDTH] |= (1 << (y % 8));
    } else {
        SSD1306_Buffer[x + (y / 8) * SSD1306_WIDTH] &= ~(1 << (y % 8));
    }
}

void SSD1306_UpdateScreen(void)
{
    /* 设置列地址范围 */
    ssd1306_WriteCmd(0x21);
    ssd1306_WriteCmd(0);
    ssd1306_WriteCmd(127);

    /* 设置页地址范围 */
    ssd1306_WriteCmd(0x22);
    ssd1306_WriteCmd(0);
    ssd1306_WriteCmd(7);

    /* 发送显存数据 */
    ssd1306_WriteData(SSD1306_Buffer, sizeof(SSD1306_Buffer));
}

/*=================== 绘图函数 ===================*/

void SSD1306_DrawLine(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, uint8_t color)
{
    int16_t dx = abs(x1 - x0);
    int16_t dy = -abs(y1 - y0);
    int16_t sx = (x0 < x1) ? 1 : -1;
    int16_t sy = (y0 < y1) ? 1 : -1;
    int16_t err = dx + dy;
    int16_t e2;

    for (;;) {
        SSD1306_DrawPixel(x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void SSD1306_DrawRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, uint8_t color)
{
    SSD1306_DrawLine(x, y, x + w - 1, y, color);
    SSD1306_DrawLine(x, y + h - 1, x + w - 1, y + h - 1, color);
    SSD1306_DrawLine(x, y, x, y + h - 1, color);
    SSD1306_DrawLine(x + w - 1, y, x + w - 1, y + h - 1, color);
}

void SSD1306_FillRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, uint8_t color)
{
    for (uint8_t i = x; i < x + w; i++) {
        for (uint8_t j = y; j < y + h; j++) {
            SSD1306_DrawPixel(i, j, color);
        }
    }
}

void SSD1306_DrawCircle(uint8_t x0, uint8_t y0, uint8_t r, uint8_t color)
{
    int16_t x = 0, y = r;
    int16_t d = 3 - 2 * r;

    while (x <= y) {
        SSD1306_DrawPixel(x0 + x, y0 + y, color);
        SSD1306_DrawPixel(x0 - x, y0 + y, color);
        SSD1306_DrawPixel(x0 + x, y0 - y, color);
        SSD1306_DrawPixel(x0 - x, y0 - y, color);
        SSD1306_DrawPixel(x0 + y, y0 + x, color);
        SSD1306_DrawPixel(x0 - y, y0 + x, color);
        SSD1306_DrawPixel(x0 + y, y0 - x, color);
        SSD1306_DrawPixel(x0 - y, y0 - x, color);

        if (d < 0) {
            d = d + 4 * x + 6;
        } else {
            d = d + 4 * (x - y) + 10;
            y--;
        }
        x++;
    }
}

/*=================== 字体绘制 ===================*/

/**
 * @brief 绘制字符
 * @note  字体数据格式: 逐列排列, 每列1字节(8像素)
 *         7x10 字体: 每字符 7列 × 10行(2字节/列)
 *         11x18 字体: 每字符 11列 × 18行(3字节/列)
 */
void SSD1306_DrawChar(uint8_t x, uint8_t y, char ch, const SSD1306_Font_t *font)
{
    if (ch < 32 || ch > 126) ch = '?';
    uint16_t idx = (ch - 32) * font->width;

    for (uint8_t col = 0; col < font->width; col++) {
        for (uint8_t row = 0; row < font->height; row++) {
            uint16_t byte_idx = idx + col;
            uint8_t bit = (font->data[byte_idx] >> (row % 8)) & 1;
            if (row % 8 == 0 && row > 0) byte_idx += (font->width * 8); /* 跨页 */
            /* 简化: 对单字节高度字体 (≤8) 直接画 */
            if (font->height <= 8) {
                if (font->data[idx + col] & (1 << row)) {
                    SSD1306_DrawPixel(x + col, y + row, SSD1306_COLOR_WHITE);
                }
            }
        }
    }
}

void SSD1306_DrawString(uint8_t x, uint8_t y, const char *str, const SSD1306_Font_t *font)
{
    while (*str) {
        SSD1306_DrawChar(x, y, *str, font);
        x += font->width + 1;
        if (x + font->width >= SSD1306_WIDTH) {
            x = 0;
            y += font->height + 2;
        }
        str++;
    }
}

